class CompositeTask < Task
  def initialize(name)
    super(name)
    @sub_tasks = []
  end

  def add_sub_task(task)
    @sub_tasks << task
    task.parent = self
  end

  def remove_sub_task(task)
    @sub_tasks.delete(task)
    task.parent = nil
  end

  def get_time_required
    @sub_tasks.map { |task| task.get_time_required }.inject(:+) || 0
  end

  def [](index)
    @sub_tasks[index]
  end

  def []=(index, new_value)
    @sub_tasks[index] = new_value
  end

  def total_num_of_tasks
    @sub_tasks.map(&:total_num_of_tasks).inject(:+) || 0
  end

  alias << add_sub_task
end
